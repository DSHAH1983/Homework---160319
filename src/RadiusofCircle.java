public class RadiusofCircle
{ //this method is to show area of circle for given radius 6
    // A = pi * radius * radius
    static int radius = 6;
    //this is set value of pi
    static final double PI = 3.14;

    public static void main(String [] args)
    {
        //pi formula is 3.14*6*6
        System.out.println(PI*radius*radius);
    }
}
